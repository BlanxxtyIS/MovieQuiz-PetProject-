import UIKit

extension UIColor {
    static let theme = ColorTheme()
}

struct ColorTheme {
    let bBackground = UIColor(named: "bBackground")
    let bBlack = UIColor(named: "bBlack")
    let bGray = UIColor(named: "bGray")
    let bGreen = UIColor(named: "bGreen")
    let bRed = UIColor(named: "bRed")
    let bWhite = UIColor(named: "bWhite")
}

